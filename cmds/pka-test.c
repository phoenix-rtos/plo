static __attribute__((unused)) void print_buf(const u8 *buf, size_t size)
{
	for (size_t i = 0; i < size; i++) {
		lib_printf("%02x ", buf[i]);
		if (i % 16 == 15) {
			lib_printf("\n");
		}
	}
}


